    ID int NOT NULL PRIMARY KEY,
    City nvarchar(50),
	Team_Name nvarchar(50),
    QB nchar(10),
	RB nchar(10),
	WR nchar(10),
	TE nchar(10),
	OL nchar(10),
	DL nchar(10),
	LB nchar(10),
	CB nchar(10),
	S nchar(10),
	K nchar(10),
	Coach nchar(10),
	Momentum nchar(10),
    Timezone int
);